def tribonacci(n): 

    if n == 0: # если 0 то вернем 0 

        return 0 

    if n == 1: # если 1 то вернем 1 

        return 1 

    if n == 2: # если 2 то вернем 1 

        return 1 

    dp = [0] * (n + 1) # Создаем список для ответа 

    dp[1] = 1# Первый и вотрой элемент равен 1 

    dp[2] = 1 

  

    for i in range(3, n + 1):#обход 

        dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3] # Складываем 3 последние элемента 

             

             

"""Сложность O(n)"""